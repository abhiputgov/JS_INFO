//while loop
let i = 0;

while(i < 3){
    console.log(i);
    i++;
}

console.log("--------------------------------------------------------------------------------------------------------------------------------------");

// breaking the loop
let sum = 0;

while(true){
    let value = "";
    if(!value) break;
    sum += value;
}

console.log(sum);

console.log("--------------------------------------------------------------------------------------------------------------------------------------");

// continuing the loop
for (let index = 0; index < 10; index++) {
    if (index % 2 === 0) {
        continue;
    }
    
    console.log(index);
    
}

console.log("--------------------------------------------------------------------------------------------------------------------------------------");